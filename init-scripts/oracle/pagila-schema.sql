CREATE TABLE pagila.CUSTOMER_LOG (
    audit_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    operation VARCHAR2(10) NOT NULL,
    old_value CLOB,
    new_value CLOB,
    edition_date TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE OR REPLACE TRIGGER trg_audit_customer
BEFORE INSERT OR UPDATE OR DELETE ON pagila.customer
FOR EACH ROW
DECLARE
    v_old CLOB;
    v_new CLOB;
    v_operation VARCHAR2(10);
BEGIN
    IF DELETING THEN
        v_operation := 'DELETE';
        v_new := NULL;
        v_old := JSON_OBJECT(
            'CUSTOMER_ID' VALUE :OLD.customer_id,
            'STORE_ID' VALUE :OLD.store_id,
            'FIRST_NAME' VALUE :OLD.first_name,
            'LAST_NAME' VALUE :OLD.last_name,
            'EMAIL' VALUE :OLD.email,
            'ADDRESS_ID' VALUE :OLD.address_id,
            'ACTIVEBOOL' VALUE :OLD.activebool,
            'CREATE_DATE' VALUE :OLD.create_date,
            'LAST_UPDATE' VALUE :OLD.last_update,
            'ACTIVE' VALUE :OLD.active
        );
    ELSIF UPDATING THEN
        v_operation := 'UPDATE';
        v_new := JSON_OBJECT(
            'CUSTOMER_ID' VALUE :NEW.customer_id,
            'STORE_ID' VALUE :NEW.store_id,
            'FIRST_NAME' VALUE :NEW.first_name,
            'LAST_NAME' VALUE :NEW.last_name,
            'EMAIL' VALUE :NEW.email,
            'ADDRESS_ID' VALUE :NEW.address_id,
            'ACTIVEBOOL' VALUE :NEW.activebool,
            'CREATE_DATE' VALUE :NEW.create_date,
            'LAST_UPDATE' VALUE :NEW.last_update,
            'ACTIVE' VALUE :NEW.active
        );
        v_old := JSON_OBJECT(
            'CUSTOMER_ID' VALUE :OLD.customer_id,
            'STORE_ID' VALUE :OLD.store_id,
            'FIRST_NAME' VALUE :OLD.first_name,
            'LAST_NAME' VALUE :OLD.last_name,
            'EMAIL' VALUE :OLD.email,
            'ADDRESS_ID' VALUE :OLD.address_id,
            'ACTIVEBOOL' VALUE :OLD.activebool,
            'CREATE_DATE' VALUE :OLD.create_date,
            'LAST_UPDATE' VALUE :OLD.last_update,
            'ACTIVE' VALUE :OLD.active
        );
    ELSE
        v_operation := 'INSERT';
        v_new := JSON_OBJECT(
            'CUSTOMER_ID' VALUE :NEW.customer_id,
            'STORE_ID' VALUE :NEW.store_id,
            'FIRST_NAME' VALUE :NEW.first_name,
            'LAST_NAME' VALUE :NEW.last_name,
            'EMAIL' VALUE :NEW.email,
            'ADDRESS_ID' VALUE :NEW.address_id,
            'ACTIVEBOOL' VALUE :NEW.activebool,
            'CREATE_DATE' VALUE :NEW.create_date,
            'LAST_UPDATE' VALUE :NEW.last_update,
            'ACTIVE' VALUE :NEW.active
        );
        v_old := NULL;
    END IF;

    INSERT INTO CUSTOMER_LOG(operation, old_value, new_value)
    VALUES (v_operation, v_old, v_new);
END;
/

