CREATE TABLE pagila.country (
    country_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    country VARCHAR2(100) NOT NULL,
    last_update TIMESTAMP with time zone DEFAULT CURRENT_TIMESTAMP NOT NULL
);

CREATE TABLE pagila.city (
    city_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    city VARCHAR2(100) NOT NULL,
    country_id NUMBER NOT NULL,
    last_update TIMESTAMP with time zone DEFAULT CURRENT_TIMESTAMP NOT NULL,
    CONSTRAINT fk_city_country FOREIGN KEY (country_id) REFERENCES country(country_id)
);

CREATE TABLE pagila.language (
    language_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name CHAR(20) NOT NULL,
    last_update TIMESTAMP with time zone DEFAULT CURRENT_TIMESTAMP NOT NULL
);

CREATE TABLE pagila.address (
    address_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    address VARCHAR2(100) NOT NULL,
    address2 VARCHAR2(100),
    district VARCHAR2(100) NOT NULL,
    city_id NUMBER NOT NULL,
    postal_code VARCHAR2(100),
    phone VARCHAR2(100) NOT NULL,
    last_update TIMESTAMP with time zone DEFAULT CURRENT_TIMESTAMP NOT NULL,
    CONSTRAINT fk_address_city FOREIGN KEY (city_id) REFERENCES city(city_id)
);

CREATE TABLE pagila.actor (
    actor_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    first_name VARCHAR2(100) NOT NULL,
    last_name VARCHAR2(100) NOT NULL,
    last_update TIMESTAMP with time zone DEFAULT CURRENT_TIMESTAMP NOT NULL
);

CREATE TABLE pagila.category (
    category_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR2(100) NOT NULL,
    last_update TIMESTAMP with time zone DEFAULT CURRENT_TIMESTAMP NOT NULL
);

CREATE TABLE pagila.store (
    store_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    manager_staff_id NUMBER,
    address_id NUMBER NOT NULL,
    last_update TIMESTAMP with time zone DEFAULT CURRENT_TIMESTAMP NOT NULL,
    CONSTRAINT fk_store_address FOREIGN KEY (address_id) REFERENCES address(address_id)
);

CREATE TABLE pagila.staff (
    staff_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    first_name VARCHAR2(100) NOT NULL,
    last_name VARCHAR2(100) NOT NULL,
    address_id NUMBER NOT NULL,
    email VARCHAR2(100),
    store_id NUMBER NOT NULL,
    active boolean DEFAULT true NOT NULL,
    username VARCHAR2(100) NOT NULL,
    password VARCHAR2(100),
    last_update TIMESTAMP with time zone DEFAULT CURRENT_TIMESTAMP NOT NULL,
    CONSTRAINT fk_staff_address FOREIGN KEY (address_id) REFERENCES address(address_id),
    CONSTRAINT fk_staff_store FOREIGN KEY (store_id) REFERENCES store(store_id)
);

CREATE TABLE pagila.film (
    film_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    title VARCHAR2(100) NOT NULL,
    description VARCHAR2(100),
    release_year NUMBER,
    language_id NUMBER NOT NULL,
    original_language_id NUMBER,
    rental_duration smallint DEFAULT 3 NOT NULL,
    rental_rate NUMBER(4,2) DEFAULT 4.99 NOT NULL,
    length smallint,
    replacement_cost NUMBER(5,2) DEFAULT 19.99 NOT NULL,
    rating VARCHAR(6) DEFAULT 'G',
    last_update TIMESTAMP with time zone DEFAULT CURRENT_TIMESTAMP NOT NULL,
    CONSTRAINT fk_film_language FOREIGN KEY (language_id) REFERENCES language(language_id),
    CONSTRAINT fk_film_original_language FOREIGN KEY (original_language_id) REFERENCES language(language_id)
);

CREATE TABLE pagila.film_actor (
    actor_id NUMBER NOT NULL,
    film_id NUMBER NOT NULL,
    last_update TIMESTAMP with time zone DEFAULT CURRENT_TIMESTAMP NOT NULL,
    CONSTRAINT fk_film_actor_actor FOREIGN KEY (actor_id) REFERENCES actor(actor_id),
    CONSTRAINT fk_film_actor_film FOREIGN KEY (film_id) REFERENCES film(film_id)
);

CREATE TABLE pagila.film_category (
    film_id NUMBER NOT NULL,
    category_id NUMBER NOT NULL,
    last_update TIMESTAMP with time zone DEFAULT CURRENT_TIMESTAMP NOT NULL,
    CONSTRAINT fk_film_category_film FOREIGN KEY (film_id) REFERENCES film(film_id),
    CONSTRAINT fk_film_category_category FOREIGN KEY (category_id) REFERENCES category(category_id)
);

CREATE TABLE pagila.inventory (
    inventory_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    film_id NUMBER NOT NULL,
    store_id NUMBER NOT NULL,
    last_update TIMESTAMP with time zone DEFAULT CURRENT_TIMESTAMP NOT NULL,
    CONSTRAINT fk_inventory_film FOREIGN KEY (film_id) REFERENCES film(film_id),
    CONSTRAINT fk_inventory_store FOREIGN KEY (store_id) REFERENCES store(store_id)
);

